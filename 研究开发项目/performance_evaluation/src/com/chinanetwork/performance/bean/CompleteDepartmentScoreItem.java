package com.chinanetwork.performance.bean;
/**
 * 用于封装在部门打分记录表的数据
 * 	包括部门评分条目 以及 所得的分数
 * @author Administrator
 *
 */
public class CompleteDepartmentScoreItem {

	private DepartmentScoreItem departmentScoreItem;	//部门评分条目
	private float score;								//每一个条目对应的打分
	private int modifyTimes;
	
	public int getModifyTimes() {
		return modifyTimes;
	}

	public void setModifyTimes(int modifyTimes) {
		this.modifyTimes = modifyTimes;
	}

	public CompleteDepartmentScoreItem(){}
	
	public CompleteDepartmentScoreItem(DepartmentScoreItem departmentScoreItem,float score){
		this.departmentScoreItem=departmentScoreItem;
		this.score=score;
	}

	public DepartmentScoreItem getDepartmentScoreItem() {
		return departmentScoreItem;
	}

	public void setDepartmentScoreItem(DepartmentScoreItem departmentScoreItem) {
		this.departmentScoreItem = departmentScoreItem;
	}

	public float getScore() {
		return score;
	}

	public void setScore(float score) {
		this.score = score;
	}
	
	
}
