package com.chinanetwork.performance.bean;

import java.util.Date;

public class StaffScoreRecord {
	
	
	private int id; 
	private Staff scoredFromStaff;
	private Staff scoredToStaff;
	private StaffScoreItem staffScoreItem;
	private float score;
	private Date date;
	private int whichYear;
	private int whichMonth;
	private int modifyTimes;
	
	public int getModifyTimes() {
		return modifyTimes;
	}
	public void setModifyTimes(int modifyTimes) {
		this.modifyTimes = modifyTimes;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Staff getScoredFromStaff() {
		return scoredFromStaff;
	}
	public void setScoredFromStaff(Staff scoredFromStaff) {
		this.scoredFromStaff = scoredFromStaff;
	}
	public Staff getScoredToStaff() {
		return scoredToStaff;
	}
	public void setScoredToStaff(Staff scoredToStaff) {
		this.scoredToStaff = scoredToStaff;
	}
	public StaffScoreItem getStaffScoreItem() {
		return staffScoreItem;
	}
	public void setStaffScoreItem(StaffScoreItem staffScoreItem) {
		this.staffScoreItem = staffScoreItem;
	}
	public float getScore() {
		return score;
	}
	public void setScore(float score) {
		this.score = score;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public int getWhichYear() {
		return whichYear;
	}
	public void setWhichYear(int whichYear) {
		this.whichYear = whichYear;
	}
	public int getWhichMonth() {
		return whichMonth;
	}
	public void setWhichMonth(int whichMonth) {
		this.whichMonth = whichMonth;
	}
	public StaffScoreRecord(int id, Staff scoredFromStaff, Staff scoredToStaff,
			StaffScoreItem staffScoreItem, float score, Date date,
			int whichYear, int whichMonth, int modifyTimes) {
		super();
		this.id = id;
		this.scoredFromStaff = scoredFromStaff;
		this.scoredToStaff = scoredToStaff;
		this.staffScoreItem = staffScoreItem;
		this.score = score;
		this.date = date;
		this.whichYear = whichYear;
		this.whichMonth = whichMonth;
		this.modifyTimes = modifyTimes;
	}
	public StaffScoreRecord() {
		super();
	}


	
}
